def func_completion(a, b, x, y, z):
    print("Addition of %2d and %2d is: %2d" %(a, b, x))
    print("Multiplication of %2d and %2d is: %2d" %(a, b, y))
    print("Square root of %2d plus %2d is: %5.2f" %(a, b, z))
    return ""